#[cfg(test)]
mod tests {
    use crate::consts::SERIAL_INBOUND;
    use crate::serial_actor::{DecodeError, SerialFrame, decode_frame};

    #[test]
    fn decode_frame_full() {
        let frame = [SERIAL_INBOUND, 0x05, 0x00, 0x04, 0x01, 0x02, 0x03, 0x04];
        let result = decode_frame(&frame);
        assert_eq!(
            result,
            Ok(SerialFrame {
                delimiter: SERIAL_INBOUND,
                frame_length: 5,
                frame: vec![0x04, 0x01, 0x02, 0x03, 0x04]
            })
        );
    }
    #[test]
    fn decode_frame_short() {
        let frame = [SERIAL_INBOUND, 0x05, 0x00, 0x04, 0x01, 0x02, 0x03];
        let result = decode_frame(&frame);
        assert_eq!(result, Err(DecodeError::FrameTooShort));
    }
    #[test]
    fn decode_frame_long() {
        let frame = [
            SERIAL_INBOUND,
            0x05,
            0x00,
            0x04,
            0x01,
            0x02,
            0x03,
            0x04,
            0x05,
            0x06,
            0x07,
        ];
        let result = decode_frame(&frame);
        assert_eq!(result, Err(DecodeError::FrameTooLong));
    }
    #[test]
    fn decode_real_frame() {
        let frame = [
            0x3e, 0x40, 0x00, 0x05, 0x01, 0x16, 0x16, 0xd7, 0xda, 0xec, 0x8c, 0x18, 0x02, 0x3e,
            0x87, 0x92, 0xa4, 0xb8, 0x5b, 0xae, 0x67, 0x49, 0x84, 0x9a, 0xb0, 0x7d, 0x3a, 0x3c,
            0xeb, 0xed, 0xf5, 0xc4, 0xdd, 0x8b, 0xe2, 0x48, 0x43, 0xdb, 0x78, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xbd, 0xe4, 0x0d, 0x00, 0x24,
            0xf4, 0x00, 0x00, 0x07, 0x05, 0x50, 0x65, 0x74, 0x65, 0x50, 0x43,
        ];
        let result = decode_frame(&frame);
        assert_eq!(result, Err(DecodeError::FrameTooLong));
    }

    #[test]
    fn decode_frame_88() {
        let frame = [
            0x88, 0x2d, 0xad, 0x15, 0x01, 0x06, 0x11, 0x28, 0x31, 0xc9, 0xf7, 0xcc, 0x96, 0x5c,
            0xb2, 0xdc, 0x65, 0x08, 0xe1, 0x43, 0xd2, 0x46, 0x83, 0x51, 0xeb,
        ];
        let result = decode_frame(&frame);
        assert_eq!(result, Err(DecodeError::FrameTooLong));
    }
}
